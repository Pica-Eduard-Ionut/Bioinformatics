def getRepeats(seq):
    seq = seq.replace("\n", "")
    repeated = {}

    for length in range(6, 11):
        subSequences = [seq[i:i+length] for i in range(len(seq) - length + 1)]
        counts = Counter(subSequences)
        repeated[length] = {sub: count for sub, count in counts.items() if count > 1}

    return repeated

